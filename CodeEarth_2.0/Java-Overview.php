<h1 align="center">Java - Overview</h1>
<hr> 
<p>Java programming language was originally developed by Sun Microsystems which was initiated by James 
Gosling and released in 1995 as core component of Sun Microsystems' Java platform (Java 1.0 [J2SE]).
</p>

<p>The latest release of the Java Standard Edition is Java SE 8. With the advancement of Java and its 
widespread popularity, multiple configurations were built to suite various types of platforms.
Ex: J2EE for Enterprise Applications, J2ME for Mobile Applications.
</p>

<p>The new J2 versions were renamed as Java SE, Java EE and Java ME respectively. 
Java is guaranteed to be <b>Write Once, Run Anywhere.</b>
</p>

<p>Java is:</p>
<ul>
	<li>
			<b>Object Oriented:</b>In Java, everything is an Object. Java can be easily extended since it is based on the Object model.
		
	</li>
	<br>
	<li>
			<b>Platform independent:</b>Unlike many other programming languages including C and C++, when Java is compiled, it is not 
			compiled into platform specific machine, rather into platform independent byte code. This byte code is distributed over 
			the web and interpreted by virtual Machine (JVM) on whichever platform it is being run.
		
	</li>
	<br>
	<li>
			<b>Simple:</b>Java is designed to be easy to learn. If you understand the basic concept of OOP Java would be easy to master.
		
	</li>
	<br>
	<li>
			<b>Secure:</b>With Java's secure feature it enables to develop virus-free, tamper-free systems.
			Authentication techniques are based on public-key encryption.
		
	</li>
	<br>
	<li>
			<b>Architectural-neutral:</b>Java compiler generates an architecture-neutral object file format which makes the compiled 
			code to be executable on many processors, with the presence of Java runtime system.
		
	</li>
	<br>
	<li>
			<b>Portable:</b>Being architectural-neutral and having no implementation dependent aspects of the specification makes Java portable. 
			Compiler in Java is written in ANSI C with a clean portability boundary which is a POSIX subset.
		
	</li>
	<br>
	<li>
			<b>Robust:</b>Java makes an effort to eliminate error prone situations by emphasizing mainly on compile time error 
			checking and runtime checking.
		
	</li>
	<br>
	<li>
			<b>Multithreaded:</b>With Java's multithreaded feature it is possible to write programs that can do many tasks simultaneously. 
			This design feature allows developers to construct smoothly running interactive applications.
		
	</li>
	<br>
	<li>
			<b>Interpreted:</b>Java byte code is translated on the fly to native machine instructions and is not stored anywhere. 
			The development process is more rapid and analytical since the linking is an incremental and light weight process.
		
	</li>
	<br>
	<li>
			<b>High Performance:</b>With the use of Just-In-Time compilers, Java enables high performance.
		
	</li>
	<br>
	<li>
			<b>Distributed:</b>Java is designed for the distributed environment of the internet.
		
	</li>
	<br>
	<li>
			<b>Dynamic:</b>Java is considered to be more dynamic than C or C++ since it is designed to adapt to an evolving environment. 
			Java programs can carry extensive amount of run-time information that can be used to verify and resolve accesses to objects on run-time.
		
	</li>
	
</ul>
	<h2>History of Java:</h2>
	<p>James Gosling initiated the Java language project in June 1991 for use in one of his many set-top box projects. 
	The language, initially called Oak after an oak tree that stood outside Gosling's office, also went by the name 
	Green and ended up later being renamed as Java, from a list of random words.
	</p>
	
	<p>Sun released the first public implementation as Java 1.0 in 1995. It promised Write Once, Run Anywhere(WORA), 
	providing no-cost run-times on popular platforms.
	</p>
	
	<p>On 13 November 2006, Sun released much of Java as free and open source software under the terms of the 
	GNU General Public License (GPL).
	</p>
	
	<p>On 8 May 2007, Sun finished the process, making all of Java's core code free and open-source, 
	aside from a small portion of code to which Sun did not hold the copyright.
	</p>
	
