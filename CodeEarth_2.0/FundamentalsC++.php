	    <p class="sansserif" align="center-left">C++ is a statically typed, compiled, general-purpose, case-sensitive, free-form programming language that supports procedural, object-oriented, and generic programming.
           C++ is regarded as a middle-level language, as it comprises a combination of both high-level and low-level language features. 
           C++ was developed by Bjarne Stroustrup starting in 1979 at Bell Labs in Murray Hill, New Jersey, as an enhancement to the C language and originally named C with Classes but later it was renamed C++ in 1983.
           C++ is a superset of C, and that virtually any legal C program is a legal C++ program.The most important thing to do when learning C++ is to focus on concepts and not get lost in language technical details.
           The purpose of learning a programming language is to become a better programmer; that is, to become more effective at designing and implementing new systems and at maintaining old ones.
     	 C++ supports a variety of programming styles. You can write in the style of Fortran, C, Smalltalk, etc., in any language. Each style can achieve its aims effectively while maintaining runtime and space efficiency.</p>
